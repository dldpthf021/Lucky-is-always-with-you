adminBtn.addEventListener("click", () => {
  const pass = prompt("관리자 비밀번호를 입력하세요:");
  if(pass === "admin1234") {
    adminPanel.classList.toggle("hidden");
    updateFortuneList();
  } else {
    alert("비밀번호가 틀렸습니다!");
  }
});

// 관리자 패널 리스트 업데이트
function updateFortuneList() {
  fortuneList.innerHTML = "";
  fortunes.forEach((f,i) => {
    const li = document.createElement("li");
    li.textContent = f;

    const delBtn = document.createElement("button");
    delBtn.textContent = "삭제";
    delBtn.onclick = () => {
      fortunes.splice(i,1);
      updateFortuneList();
    };

    li.appendChild(delBtn);
    fortuneList.appendChild(li);
  });
}

// 운세 추가
addBtn.addEventListener("click", () => {
  const text = newFortuneInput.value.trim();
  if(text) {
    fortunes.push(text);
    newFortuneInput.value = "";
    updateFortuneList();
  }
});

// 모든 운세 삭제
clearBtn.addEventListener("click", () => {
  if(confirm("정말 모든 운세를 삭제하시겠습니까?")) {
    fortunes = [];
    updateFortuneList();
  }
});
